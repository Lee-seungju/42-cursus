#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*a;

	if (lst == 0 || f == 0)
		return ;
	while (lst)
	{
		a = lst->next;
		f(lst->content);
		lst = a;
	}
}
